/// @fn scanchar
/// @param[out] scan   A buffer into which to put ' ' and '*'
/// @param[ in] code   The character for which display is requested
/// @param[ in] line   The vertical position of the scan line
/// @retval     scan   The input buffer, modified with ' ' and '*'
/// @brief This encodes one scan line for 1 character.
inline char *
scanchar( char *scan, const unsigned code, const unsigned line ) {
  const char bit[ 2 ] = { ' ', '*' };
  scan[ 0 ] = bit[ !!(BIOSfont::BIOSfont[ code ][ line ] & 0x01) ];
  scan[ 1 ] = bit[ !!(BIOSfont::BIOSfont[ code ][ line ] & 0x02) ];
  scan[ 2 ] = bit[ !!(BIOSfont::BIOSfont[ code ][ line ] & 0x04) ];
  scan[ 3 ] = bit[ !!(BIOSfont::BIOSfont[ code ][ line ] & 0x08) ];
  scan[ 4 ] = bit[ !!(BIOSfont::BIOSfont[ code ][ line ] & 0x10) ];
  scan[ 5 ] = bit[ !!(BIOSfont::BIOSfont[ code ][ line ] & 0x20) ];
  scan[ 6 ] = bit[ !!(BIOSfont::BIOSfont[ code ][ line ] & 0x40) ];
  scan[ 7 ] = bit[ !!(BIOSfont::BIOSfont[ code ][ line ] & 0x80) ];
  return scan;
}

/*****************************************************************************/
/// @fn scanline
/// @param[out] cells  A buffer into which to put ' ' and '*'
/// @param[ in] code   The character starting a group of 8 to display
/// @param[ in] line   The vertical position of the scan line
/// @retval     cells  The input buffer, modified with ' ' and '*'
/// @brief This encodes one scan line for 8 characters across.
inline char *
scanline( char *cells, const unsigned start, const unsigned line ) {
  scanchar( cells + 1 + 9 * 0, start + 0, line );
  scanchar( cells + 1 + 9 * 1, start + 1, line );
  scanchar( cells + 1 + 9 * 2, start + 2, line );
  scanchar( cells + 1 + 9 * 3, start + 3, line );
  scanchar( cells + 1 + 9 * 4, start + 4, line );
  scanchar( cells + 1 + 9 * 5, start + 5, line );
  scanchar( cells + 1 + 9 * 6, start + 6, line );
  scanchar( cells + 1 + 9 * 7, start + 7, line );
  return cells;
}

